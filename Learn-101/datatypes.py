# Data Types in Python

# Data types are the classification of data items based on their type.
# Python has various data types such as numeric, sequence, and mapping types.

# Numeric Data Types:
# Numeric data types represent numeric values.
# They include integers, floats, and complex numbers.

# Sequence Data Types:
# Sequence data types represent data items in a sequence.
# They include strings, lists, tuples, and sets.

# Mapping Data Types:
# Mapping data types represent data items in a key-value pair.
# They include dictionaries.

# Numeric Data Types
# Integer
a = 100
print(a, " is of type ", type(a))

# Float
b = 100.0
print(b, " is of type ", type(b))

# Complex
c = 3+4j
print(c, " is of type ", type(c))

# Sequence Data Types
# String
s = "Hello World"
print(s, " is of type ", type(s))

# List
l = [10, 20, 30, 40, 50]
print(l, " is of type ", type(l))

# Tuple
t = (10, 20, 30, 40, 50)
print(t, " is of type ", type(t))

# Set
s = {10, 20, 30, 40, 50}
print(s, " is of type ", type(s))

# Dictionary
d = {1: 10, 2: 20, 3: 30, 4: 40, 5: 50}
print(d, " is of type ", type(d))
