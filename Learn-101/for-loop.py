# A for loop iterates over a sequence of elements, such as a list, tuple, or string.
# Syntax
# for element in sequence:
#     # The body of the loop is executed for each element in the sequence.

sequence = [1, 2, 3, 4, 5]
for element in sequence:
    # The body of the loop is executed for each element in the sequence.
    print(element)
